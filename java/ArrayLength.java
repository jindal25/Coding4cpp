class ArrayLength{
	public static void main(String[] args){
		int[] a1 = new int[10];
		int[] a2 = { 1,2,3,4,5,6};
		int[] a3 = { 4,5,6,7,8,9,10};

		System.out.println( " length a1 :" +a1.length);
		System.out.println( " length a2 :" +a2.length);
		System.out.println( " length a3 :" +a3.length);
	}
}
